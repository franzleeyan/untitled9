public class Variabletest3 {
    //主入口
    public static void main(String[] args) {
        //定义变量记录电影的名字
        String name = "送初恋回家";
        //定义变量记录主演的名字
        String toter = "刘鑫 张雨提 高媛";
        //定义变量记录电影年份
        int year = 2020;
        //定义变量记录电影评分
        double grade = 9.0;

        //输出所有变量的值
        System.out.println(name);
        System.out.println(toter);
        System.out.println(year);
        System.out.println(grade);
    }
}
