package com.java.learn;

public class VariableDemo1 {
    //主入口
    public static void main(String[] args) {
        //定义变量
        //数据类型 变量名 = 数据值；
        //int(整数) double(小数)
        //变量名：就是存储空间的名字
        //作用：方便以后使用
        //数据值：真正存在变量中的数据

        //等号：赋值。把右边的数据赋值给左边的变量

        int a = 10;
        //给a赋值数据为10
        System.out.println(a);
        //输出a

        int b = 20;
        //给b赋值数据为20
        System.out.println(a + b);
        //输出a+b=30
    }
}
