public class ArithmeticoperatorDemo5 {
    public static void main(String[] args) {
        //++ 和 --
        int a = 10;
        //表示把变量a里面的值+1
        a++;
        System.out.println(a);
        //表示把变量a里面的值+1
        ++a;
        System.out.println(a);
        //表示把变量a里面的值-1
        a--;
        System.out.println(a);
        //表示把变量a里面的值-1
        --a;
        System.out.println(a);
    }

}
