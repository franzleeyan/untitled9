public class Variabletest2 {
    //主入口
    public static void main(String[] args) {
        //定义变量记录姓名
        String name = "黑马谢广坤";
        //定义变量记录年龄
        int age = 18;
        //定义变量记录性别
        char gender = '男';
        //定义变量记录身高
        double height = 180.1;
        //定义变量记录是否单身
        //true是单身  false是非单身
        boolean flag = true;

        //输出变量值
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flag);
    }
}
